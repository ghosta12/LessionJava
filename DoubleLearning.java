package PracticeJavaBasics;

public class DoubleLearning {

    public static void main(String args[]) {
        int var;
        double x;

        var = 10;
        x = 10.0;

        System.out.println("Initial value of var: " + var);
        System.out.println("Initial value of x: " + x);

        var = var / 4;
        x = x / 4;

        System.out.println("var divided by 4: " + var);
        System.out.println("x divided by 4: " + x);
    }
}
