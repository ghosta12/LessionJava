package PracticeJavaBasics;

public class GallonesAndLittres {
    public static void main(String args[]){

        double littres;
        double littresPerGallones = 3.145434;
        int numberOfGallones;


        numberOfGallones = 10;
        littres = numberOfGallones * littresPerGallones;

        System.out.println("10 gallons contains " + littres + " littres");

    }
}
